                <h1>The Set Up</h1>
Lincoln Douglas is composed of two debaters and a judge, each debater defending a different side of the resolution, affirmative (for) and negative (against).  Lincoln Douglas debates can happen anywhere - from classrooms to auditoriums to even hallways.  Judges are asked to keep a strong and rigorous flow of notes of what happens in each round and decide the winner at the end.  
            <br><br>
            
        Both debaters have a constructive speech which represents their first set of arguments - often pre-written.  There is a cross examination for both sides where debaters ask questions to their opponent about the argumemts their opponent made.  The round concludes with a set of rebuttals where debaters defend their positions and can go for a variety of strategies.  
            
            

