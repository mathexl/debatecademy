                <h1>CWI!</h1>
                CWI!  <b>The most essential acronym in debate.</b>  CWI stands for Claim, Warrant, and Impact and is the format of every single argument in debate.  Learn more about CWI by moving the slide to the left (you can swipe or use your left or right arrows)!
               

            </div>
            </div>
<div class="slide">
               
      <div class="rmidp">
                <h1>CLAIM</h1>
Every argument requires a claim.  For instance let's take the following claim in this series:
<br> <br>
            <b>There is a current organ shortage.</b>               

            </div>
            </div>
            
            <div class="slide">
      <div class="rmidp">
                <h1>WARRANT</h1>
But simply stating that there is a current organ shortage isn't enough in debate.  We need proof for why that is a true claim.  The reasoning or proof of any statement is known as a warrant in debate - an unwarranted claim is just as false as a blatantly false claim for the judge.  Let's add a warrant to our claim:<br> <br>
            <b>CLAIM: There is a current organ shortage.</b> <br>              
            <b>WARRANT: Statistical evidence indicates that 200 people a year do not recieve an organ when it is medically needed.</b>               

            </div>
            </div>
          
            <div class="slide">
      <div class="rmidp">
                <h1>IMPACT</h1>
Woot! Now you proved a claim.  But most arguments aren't good arguments unless they have some type of place in the round.  Simply proving that a triangle has three sides won't win debates!  Therefore, we need an impact - the implication from this claim. While debaters may debate what impacts matter (a debate that is also full of Claims, Warrants, and Impacts), all claims need to impact back to some standard.  Let's assume Lives Matter.  We will visit this more in later lessons/courses.  Here is an impact:  <br> <br>
            <b>CLAIM: There is a current organ shortage.</b> <br>              
            <b>WARRANT: Statistical evidence indicates that 200 people a year do not recieve an organ when it is medically needed.</b>     <br>          
            <b>IMPACT: People are dying due to the organ shortage.</b>               

            </div>
