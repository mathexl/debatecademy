                <h1>The Value</h1>
While in progressive LD, the value of "morality" or "justice" is often assumed, the value remains one of the integral parts of Lincoln Douglas.  The Value is what matters in the round.  For instance, you can say that Morality is the value because the resolution talks about state policy action which should be by definition moral.  
               

