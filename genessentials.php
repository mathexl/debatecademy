                <h1>What's Lincoln Douglas?</h1>
 As defined by the National Speech and Debate Association, Lincoln Douglas is:  "In this one-on-one format, students debate a topic provided by the National Speech and Debate Association. Topics range from individual freedom versus the collective good to economic development versus environmental protection. Students may consult evidence gathered prior to the debate but may not use the Internet in round. An entire debate is roughly 45 minutes and consists of constructive speeches, rebuttals, and cross-examination."
       </div>
