            <div class="rmidp">
                <h1>Value Criterion/Standards</h1>
What is really important is the value criterion, or the new contemporary term, standard.  The Standard tells us what impacts matter.  Lincoln Douglas by its nature is a philosophical forum so often the debate about the standard can encompass a majority of the round.  For instance, under some ethical theories such as utilitarianism, our previous impact of saving lives would matter.  If the ethical standard was "respecting the constitution," different impacts would matter. 
               

            </div>

