            <div class="rmidp">
                <h1>Voting Issues</h1>
Lastly, a common formality of debate is telling the judge at the ending why they should vote for you.  Provide analysis for why you are winning on either your or their standard (or both!) and for why that means the ballot should be signed in your favor.
               

