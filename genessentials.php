            <div class="rmidp">
                <h1>Contention Level</h1>
The contention level is the next part of the case.  The contention level signifies reasons for why your side of the resolution impacts best back to your standard.  For instance, if your value criterion/standard was "respecting international law," you would win the round on the affirmative by correctly proving that the affirmative side respects international law better than the negative.  The negative can, however, challege your standard.  If they win that a better standard is respecting human lives, you can still win this debate by impacting back to that standard.  Strategically thinking about what arguments to go for is a major part about debate!
               

            </div>

