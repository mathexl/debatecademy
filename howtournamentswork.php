                <h1>Out-Rounds</h1>
At larger tournaments, awards are not determined solely by the ranking of the debaters at the end of the round (ranking is done by wins and then speaker points, a subject explored more in the next course).  Instead, a top group of debaters are paired in an elimination style bracket that is a lot similar to sports brackets.  As the rounds go, remaining debaters reach "quarters" "semis" and eventually "finals" whose winner will determine the champion of the tournament.  

               

