                <h1>Pairings</h1>
When you attend a tournament, near the start time a pairing of each round will be posted.  It lists all the codes of the debaters and whom the debater is debating.  There is a listed start time and the name of your judge.  At certain larger tournaments that feature an abundance of routine judges, you can find the judges preferences/paradigm of judging a round online, commonly found on <a href="http://judgephilosophies.wikispaces.com/">JudgePhilosophies.</a>                   

               

