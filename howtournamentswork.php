                <h1>Points/Bids</h1>
Debate tournaments are almost all associated with the National Forensics League, now know as the National Speech and Debate Association.  For every win at a debate tournament, if your school is a member, you are awards 6 NSDA Points and for every loss you are awarded 6 NSDA Points.  Outside of the NSDA, the progressive Lincoln Douglas circuit follows the Tournament of Champions bidding list and the National Debate Coaches Association points as well as any small state circuit qualifying systems.  There will be more on these supratournament ranking/points systems in later classes.               

               

