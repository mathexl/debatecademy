                <h1>Registration</h1>
           Registering for tournaments is a daunting task.  Often, tournaments do not allow independent entries and the team must be entered by a school official.  However, on occasion, tournament directors are open to indepedent entries - something you should contact them individually about.  It is best, however, that students come with thier school.  Registration of tournaments happens in various fashions, but the main two websites supporting registration are Joy of Tournaments and Tabroom.com.  Links to both of those resources can be found on the left. 
               
               <br><br>
A common protocol when registering is bringing a judge.  Judges at national tournaments often have prior debate experience but it is common to find parents among the judging pool at local tournaments.  Debaters must bring judges to fulfill their entry - while your judge cannot judge you, they add to the pool of judges available to judge other rounds.  Certain tournaments let you hire a judge via paying a fee to the tournament registration.  
               

